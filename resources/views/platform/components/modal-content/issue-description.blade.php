<p class="mb-3">
    There are two different ways in which an achievement is considered broken. Use this dropdown to
    indicate which of these two types, documented below, is applicable to your situation.
  </p>
  
  <h4>Triggered at the Wrong Time</h4>
  
  <p class="mb-3">
    Use this when an achievement notification appears in your emulator at a time different than what
    is implied in the achievement's description. For instance, if the triggered achievement asks you
    to collect 10 of a collectable but appears when you haven't done so, then you would use this type.
  </p>
  
  <h4>Doesn't Trigger</h4>
  
  <p class="mb-3">
    Use this when an achievement notification <i>doesn't</i> appear when you have done what the
    achievement's description asked you to do. For instance, if the achievement you expected to
    trigger asks you to collect 10 of a collectable and you got 11, then you would use this type if
    you don't see the notification appear.
  </p>
  
  <h4>What if the achievement triggered but doesn't show up as earned for me on the site?</h4>
  
  <p class="mb-3">
    This normally indicates a network issue&mdash;which is to say that either your connection was
    interrupted while the achievement triggered or the site itself is down.
    <strong>Please do not create a ticket if this has happened to you!</strong> Instead, please
    request a manual unlock on the RetroAchievements Discord server and provide the screenshot created
    when the achievement triggered or some other form of proof.
  </p>
  