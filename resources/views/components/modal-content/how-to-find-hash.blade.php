<p class="mb-6">
  The RetroAchievements hash uniquely identifies your ROM. Specifying the hash of your ROM can help
  the developer reproduce the issue or even determine if a particular ROM is incompatible with the
  set.
</p>

<h4>Where can I find this?</h4>

<p class="mb-3">
  This is dependent on whatever emulator you're using. If you're using RetroArch, this information
  can be found by entering the RetroArch menu while your ROM is loaded and scroll down to
  Information. It will be the long alphanumeric string after "RetroAchievements Hash".
</p>

<p class="mb-3">
  If you're using RALibRetro, this information can be found by going into RetroAchievements > View
  Game Hash.
</p>
